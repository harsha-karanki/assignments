using System;

namespace Operations

{
  class Program
    {
     static void Main(string[] args)
     {
       Console.WriteLine(" x = ");
       int x = Convert.ToInt32(Console.ReadLine());
       
       
       Console.WriteLine(" y = ");
       int y = Convert.ToInt32(Console.ReadLine());
       
       
       
       Console.WriteLine( x + y );
       Console.WriteLine( x - y );
       Console.WriteLine( x * y );
       Console.WriteLine( x / y );
       Console.WriteLine( x % y );
       
     
     }
   }    
}     	